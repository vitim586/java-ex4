public class Media2 {
    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 21;
        int num3 = 22;

        // Calcular a média
        double media = (num1 + num2 + num3) / 3.0;

        // Imprimir os números escolhidos
        System.out.print("Os números escolhidos são: " + num1 + ", " + num2 + ", " + num3);

        // Imprimir a média
        System.out.print("\nA média é: " + media);
    }
}
